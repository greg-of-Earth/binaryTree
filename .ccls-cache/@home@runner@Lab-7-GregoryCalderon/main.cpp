void GetData(fstream& inData, bool& isPrep) {
  if(!isPrep){
    isFileOpen(inData);
    PrepInput(inData);  
  }
 
}

//check if file open 
void isFileOpen(fstream& inData) {
  inData.open("inpTxt.txt", ios::in| ios::out); // let file be input/output file

  if(!inData.is_open()) {
    cerr << "ERROR OPENING FILE.";
    exit(EXIT_FAILURE);
  }

  if(inData.peek() == EOF) {
    cerr << "ERROR. FILE IS EMPTY.";
    inData.close();
    exit(EXIT_FAILURE);
  }
}

// edit input file. replace punctuation with a blank space and delete 4 letter words
void PrepInput(fstream& inData) {
  int size = GetFileSize(inData);
  DelPnct(inData, size);
  DelFours(inData);
}

// get size of input file in order to loop through it 
int GetFileSize(fstream& inData) {
  inData.seekg(0,inData.end); //set *get to end of file
  int size = inData.tellg(); // return position of *get 
  inData.seekg(0, inData.beg); // reset *get to beginning of file
  return size;
}

// replace punctuation with blank space 
void DelPnct(fstream& inData, int size) {
   for(int i=0; i<size; i++) {
    char ch = inData.get();
    if(ispunct(ch)) {     
        inData.seekg(i);
        inData.put(' ');
    }
  }
  inData.seekg(0, inData.beg);
}

//delete all 4 letter words from input file
void DelFours(fstream& inData) {
  string word;
  while(inData >> word) {
    if(word.length() < 4) { // if word < 4 letters, overWrite with blank
      int size = word.length();
      inData.seekp(-size, ios_base::cur);
      inData.put(' ');
    }
  }
  inData.seekg(0, inData.beg);
}

//build a tree
void BuildTree(fstream& inData, Tree& alphaTree){
  string word;
  inData.clear();
  inData.seekg(0);
  while(inData >> word) {
   TransformToUpper(word);
   try{
     alphaTree.AddNode(word);
   }catch(bad_alloc& e){
     alphaTree.ExcptHndl();
   }
  
  }
  inData.clear();
  inData.seekg(0, inData.beg);
}

void PrintTree(Tree &alphaTree, bool& isEmpty) {
  alphaTree.PrintTree(isEmpty);
}

void DeleteWord(fstream& inData, Tree& alphaTree, bool& isEmpty){
  string delWrd;
  cout << "Delete Word: ";
  cin >> delWrd;
  TransformToUpper(delWrd);
  alphaTree.NodeSearch(delWrd, isEmpty);
  string inpWord;
  inData.clear();
  inData.seekg(0, inData.beg);

  
  while (inData >> inpWord) {

    if(inData.eof()){
// if only one word in file process it 
      inData.clear();  // Clear the end-of-file indicator
      inData.seekg(0, ios::end);
      if (TransformToUpper(inpWord) == delWrd) {
       inData.seekp(-inpWord.length(), ios::cur);
       inData << string(inpWord.length(), '?');
        inData.sync();
      }
    }else if(TransformToUpper(inpWord)==delWrd){
      inData.seekp(-inpWord.length(), ios::cur);
      inData << string(inpWord.length(), '?');    
    }
  }
  inData.clear();
  inData.seekg(0, inData.beg);
}

// transform to uppercase 
string TransformToUpper(string& term) {
  	transform(
 		term.begin(),
 		term.end(),
 		term.begin(),
 		::toupper);
  return term;
}

// menu
void MainMenu(fstream& inData, Tree& alphaTree) {
  int choice = -1;
  bool isTreeBuilt=false;
  bool isFilePrep=false;
  bool isEmpty=false;
  do {
    cout << "\n**MENU**\n"
          "1. Prepare Input\n"
          "2. Build Tree\n"
          "3. Print Tree\n"
          "4. Delete Node\n"
          "5. Quit\n"
          "Enter choice: ";
    cin >> choice;
    cout << endl;
    switch(choice) {
      case PREP_INP:
        GetData(inData, isFilePrep);
        isFilePrep=true;
        break;
      case  BUILD_TREE:
        if(isFilePrep){
          BuildTree(inData, alphaTree);
          isTreeBuilt=true;
        }else {
          cerr << "ERROR. FILE MUST BE PREPARED FIRST.\n";
        }
        
        break;
      case PRINT_TREE_ALPHA:
        if(isTreeBuilt && isFilePrep) {
          PrintTree(alphaTree, isEmpty);
        }else {
          cout << "ERROR. NO TREE HAS BEEN BUILT.\n";
        }        
        break;
      case DELETE_WRD:
        if(isTreeBuilt && !isEmpty) { 
          DeleteWord(inData, alphaTree, isEmpty);
        }else if(isEmpty){
          cout << "ERROR. TREE IS EMPTY.\n";
          //isFilePrep=false;
        }else {
          cout << "ERROR. NO TREE HAS BEEN BUILT.\n";
        }
        break;
      case QUIT:
        cout << "PROGRAM TERMINATED.\n";
        inData.close();
        break;
      default:
      cout << "\nERROR. INVALID SELECTION.\n";
			cin.clear();
			cin.ignore(numeric_limits<streamsize>::max(), '\n');
			break;
    }
  }while(choice != QUIT);
}

